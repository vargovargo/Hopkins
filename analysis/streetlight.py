def process_zone_activity():
    """
    Read *_za_ped.csv and *_za_bike.csv. Vehicles do NOT have zone activity
    files — vehicle data comes from network performance (Step 4).

    Saves:
      data/processed/za_pedestrians.csv
      data/processed/za_bicycles.csv

    Returns dict: {mode_key: (dataframe, output_unit_col)}
    """
    print("\n" + "=" * 70)
    print("STEP 2 — PROCESS ZONE ACTIVITY (pedestrian + bicycle)")
    print("=" * 70)
    print("  NOTE: No zone activity file for vehicles — vehicle volumes come")
    print("        from network performance segment metrics (Step 4).")

    results = {}

    targets = [
        ("*_za_ped.csv",  "pedestrians", OUTPUT_ZA_PEDESTRIANS),
        ("*_za_bike.csv", "bicycles",    OUTPUT_ZA_BICYCLES),
    ]

    for glob_pattern, mode_key, out_path in targets:
        files = sorted(RAW_DIR.rglob(glob_pattern))
        if not files:
            print(f"\n  [SKIP] No files matching {glob_pattern}")
            continue

        frames = []
        for fpath in files:
            df = pd.read_csv(fpath, low_memory=False)
            frames.append(df)
            print(f"\n  Loaded ({mode_key}): {fpath.name}  ({len(df)} rows)")

        combined = pd.concat(frames, ignore_index=True)

        zone_id_col   = _safe_col(combined, ["Zone ID", "zone_id"])
        zone_name_col = _safe_col(combined, ["Zone Name", "zone_name"])
        mode_col      = _safe_col(combined, ["Mode of Travel", "Mode"])
        daytype_col   = _safe_col(combined, ["Day Type", "day_type"])
        daypart_col   = _safe_col(combined, ["Day Part", "day_part"])
        period_col    = _safe_col(combined, ["Data Periods", "data_periods"])

        # Zone traffic column — should be "Average Daily Zone Traffic (StL Volume)"
        metric_col = None
        for c in combined.columns:
            if "average daily zone traffic" in c.lower() or "zone traffic" in c.lower():
                metric_col = c
                break

        missing = [n for n, v in [
            ("Zone ID", zone_id_col), ("Zone Name", zone_name_col),
            ("Mode of Travel", mode_col), ("Day Type", daytype_col),
            ("Day Part", daypart_col), ("Zone Traffic", metric_col),
        ] if v is None]
        if missing:
            print(f"  [ERROR] Missing columns for {mode_key}: {missing}")
            print(f"  Available: {list(combined.columns)}")
            sys.exit(1)

        df_out = combined[[
            zone_id_col, zone_name_col, mode_col,
            daytype_col, daypart_col, metric_col
        ]].copy()
        if period_col:
            df_out.insert(0, "data_periods", combined[period_col])

        df_out.columns = (
            (["data_periods"] if period_col else []) +
            ["zone_id", "zone_name", "mode_of_travel",
             "day_type", "day_part", "zone_traffic"]
        )
        df_out["output_unit"] = metric_col

        df_out.to_csv(out_path, index=False)
        print(f"  ✓ {mode_key}: {len(df_out)} rows → {out_path.relative_to(REPO_ROOT)}")
        print(f"    Output unit: {metric_col!r}")

        zones = sorted(df_out["zone_name"].dropna().unique().tolist())
        print(f"    Zones: {zones}")

        results[mode_key] = (df_out, metric_col)

    return results


