def build_verified_json(za_results, df_prediction, df_network, pct_cols, inferred_zones):
    """Build data/processed/streetlight_verified.json from processed data."""
    print("\n" + "=" * 70)
    print("STEP 5 — BUILD streetlight_verified.json")
    print("=" * 70)

    ped_unit  = za_results.get("pedestrians", (None, None))[1] or "not present"
    bike_unit = za_results.get("bicycles",    (None, None))[1] or "not present"

    # Derive vehicle period from network performance data
    vehicle_period = "Jan 01 2025 - Dec 31 2025"
    if df_network is not None and "data_periods" in df_network.columns:
        vals = df_network["data_periods"].dropna().astype(str)
        if len(vals):
            vehicle_period = vals.iloc[0]

    # Derive ped/bike periods from za data
    def _get_period(df):
        if df is None:
            return "unknown"
        if "data_periods" in df.columns:
            vals = df["data_periods"].dropna().astype(str)
            return vals.iloc[0] if len(vals) else "unknown"
        return "Jan 01 2022 - Aug 31 2022"

    ped_df   = za_results.get("pedestrians", (None,))[0]
    bike_df  = za_results.get("bicycles",    (None,))[0]
    ped_period  = _get_period(ped_df)
    bike_period = _get_period(bike_df)

    zones = []
    if df_network is not None and "zone_name" in df_network.columns:
        zones = sorted(df_network["zone_name"].dropna().unique().tolist())

    metadata = {
        "status": "VERIFIED",
        "generated": datetime.utcnow().isoformat() + "Z",
        "analysis_ids": {
            "vehicles": "2012902 — Hopkins Rose Cedar AV 2025",
            "pedestrians": "2013191 — Hopkins Rose Cedar Ped",
            "bicycles": "2013334 — Hopkins Rose Cedar Bike",
        },
        "output_units": {
            "vehicles": "Average Daily Segment Traffic (StL Volume) — from network performance",
            "pedestrians": ped_unit,
            "bicycles": bike_unit,
        },
        "vehicle_file_type": "network_performance_seg_metrics",
        "ped_bike_file_type": "zone_activity",
        "structural_note": (
            "Vehicle volumes are segment-based (network performance); "
            "ped/bike volumes are zone-based (zone activity). Both use StL Volume. "
            "Segment and zone boundaries roughly correspond but are not identical — "
            "do not treat as precisely equivalent geographic units."
        ),
        "modes_comparable": True,
        "comparability_note": (
            "All modes use StL Volume. Cross-mode comparison is valid in unit terms. "
            "However, vehicle data uses segment metrics and ped/bike use zone activity — "
            "this structural difference must be disclosed in any UI showing modes together."
        ),
        "data_periods": {
            "vehicles": vehicle_period,
            "pedestrians": ped_period,
            "bicycles": bike_period,
        },
        "day_parts_available": [
            "All Day", "Early AM", "Peak AM", "Mid-Day", "Peak PM", "Late PM"
        ],
        "day_types_available": ["All Days", "Weekday", "Weekend"],
        "speed_percentiles_available": sorted(list(pct_cols.keys())) if pct_cols else [],
        "zones": zones,
        "confidence_intervals_available": df_prediction is not None,
        "inferred_volume_zones": inferred_zones,
        "notes": (
            "Verified against raw Streetlight CSV exports. "
            + (f"Inferred volume zones: {inferred_zones}. " if inferred_zones else "")
        ),
    }

    # --- zone_activity (ped + bike) ---
    zone_activity = {}
    for mode_key, (df, unit) in za_results.items():
        if df is None:
            continue
        mode_data = {}
        for _, row in df.iterrows():
            zone = str(row.get("zone_name", ""))
            dt   = str(row.get("day_type", ""))
            dp   = str(row.get("day_part", ""))
            val  = row.get("zone_traffic")
            if zone not in mode_data:
                mode_data[zone] = {}
            mode_data[zone][f"{dt} / {dp}"] = val
        zone_activity[mode_key] = mode_data

    # --- prediction_intervals ---
    prediction_intervals = {}
    if df_prediction is not None:
        for _, row in df_prediction.iterrows():
            zone = str(row.get("zone_name", ""))
            ym   = str(row.get("year_month", ""))
            prediction_intervals.setdefault(zone, {})[ym] = {
                "avg_daily_volume": row.get("avg_daily_volume"),
                "lower_95":         row.get("lower_95"),
                "upper_95":         row.get("upper_95"),
            }

    # --- network_performance (vehicles) ---
    network_performance = {}
    if df_network is not None:
        for _, row in df_network.iterrows():
            zone = str(row.get("zone_name", ""))
            dt   = str(row.get("day_type", ""))
            dp   = str(row.get("day_part", ""))
            entry = {
                "segment_traffic":     row.get("segment_traffic"),
                "avg_speed_mph":       row.get("avg_speed_mph"),
                "free_flow_speed_mph": row.get("free_flow_speed_mph"),
                "free_flow_factor":    row.get("free_flow_factor"),
            }
            for pct in [5, 15, 85, 95]:
                key = f"speed_p{pct}"
                if key in df_network.columns:
                    entry[key] = row.get(key)
            if "inferred_volume" in df_network.columns:
                entry["inferred_volume"] = row.get("inferred_volume")
            network_performance.setdefault(zone, {})[f"{dt} / {dp}"] = entry

    verified = {
        "_metadata": metadata,
        "zone_activity": zone_activity,
        "prediction_intervals": prediction_intervals,
        "network_performance": network_performance,
    }

    with open(OUTPUT_VERIFIED_JSON, "w") as f:
        json.dump(verified, f, indent=2, default=str)

    print(f"  ✓ Written: {OUTPUT_VERIFIED_JSON.relative_to(REPO_ROOT)}")
    return verified


