def audit_files():
    """Walk data/raw/streetlight/ and print a complete audit of every CSV."""
    print("\n" + "=" * 70)
    print("STEP 1 — FULL FILE AUDIT")
    print("=" * 70)

    csv_files = sorted(RAW_DIR.rglob("*.csv"))
    if not csv_files:
        print(f"\n[ERROR] No CSV files found in {RAW_DIR}")
        sys.exit(1)

    audit = {}

    for fpath in csv_files:
        rel = fpath.relative_to(REPO_ROOT)
        size_kb = fpath.stat().st_size / 1024
        print(f"\n{'─'*60}")
        print(f"FILE: {rel}  ({size_kb:.1f} KB)")

        df = pd.read_csv(fpath, low_memory=False)
        cols = list(df.columns)
        print(f"  Rows: {len(df)}")
        print(f"  Columns: {cols}")

        mode_col     = next((c for c in cols if "mode" in c.lower()), None)
        daytype_col  = next((c for c in cols if "day type" in c.lower()), None)
        daypart_col  = next((c for c in cols if "day part" in c.lower()), None)
        period_col   = next((c for c in cols if "data period" in c.lower()), None)

        modes     = sorted(df[mode_col].dropna().unique().tolist())     if mode_col    else []
        day_types = sorted(df[daytype_col].dropna().unique().tolist())  if daytype_col else []
        day_parts = sorted(df[daypart_col].dropna().unique().tolist())  if daypart_col else []

        print(f"  Mode: {modes}")
        print(f"  Day Types: {day_types}")
        print(f"  Day Parts: {day_parts}")

        # Detect metric column and unit type
        metric_col  = None
        metric_type = None
        for c in cols:
            cl = c.lower()
            if "stl volume" in cl or "average daily" in cl or "segment traffic" in cl:
                metric_col  = c
                metric_type = "StL Volume"
                break
        if metric_col is None:
            for c in cols:
                cl = c.lower()
                if "calibrated index" in cl:
                    metric_col  = c
                    metric_type = "StreetLight Calibrated Index"
                    break
                elif "index" in cl and "streetlight" in cl:
                    metric_col  = c
                    metric_type = "StreetLight Index"
                    break

        print(f"  Primary metric: {metric_col!r}  →  {metric_type}")

        if period_col:
            vals = df[period_col].dropna().astype(str)
            print(f"  Data period: {vals.min()} → {vals.max()}")

        if metric_type and "Index" in metric_type:
            print("  ⚠️  WARNING: Index unit — cannot compare with Volume modes")

        audit[str(fpath)] = {
            "path": str(rel), "size_kb": round(size_kb, 1),
            "row_count": len(df), "columns": cols,
            "metric_col": metric_col, "metric_type": metric_type,
        }

    return audit


