def process_prediction_intervals():
    """
    Read *_prediction_interval*.csv (vehicle confidence intervals).
    Saves to data/processed/vehicle_prediction_intervals.csv.
    """
    print("\n" + "=" * 70)
    print("STEP 3 — PROCESS PREDICTION INTERVALS (vehicles)")
    print("=" * 70)

    # Filename: *_network_performance_seg_prediction_interval.csv (no trailing 's')
    pi_files = sorted(RAW_DIR.rglob("*_prediction_interval*.csv"))
    # Exclude monthly files for the primary output
    pi_files = [f for f in pi_files if "monthly" not in f.name.lower()]

    if not pi_files:
        print("  [SKIP] No prediction interval files found.")
        return None

    frames = []
    for fpath in pi_files:
        df = pd.read_csv(fpath, low_memory=False)
        frames.append(df)
        print(f"  Loaded: {fpath.name}  ({len(df)} rows)")
        print(f"  Columns: {list(df.columns)}")

    combined = pd.concat(frames, ignore_index=True)

    zone_id_col   = _safe_col(combined, ["Zone ID", "zone_id"])
    zone_name_col = _safe_col(combined, ["Zone Name", "zone_name"])
    period_col    = _safe_col(combined, ["Year/Month", "Data Periods", "data_periods"])

    vol_col = None
    for c in combined.columns:
        if "average daily" in c.lower() and ("volume" in c.lower() or "traffic" in c.lower()):
            vol_col = c
            break

    lower_col = upper_col = None
    for c in combined.columns:
        cl = c.lower()
        if "lower" in cl:
            lower_col = c
        elif "upper" in cl:
            upper_col = c

    print(f"  Volume column:  {vol_col!r}")
    print(f"  Lower 95% CI:   {lower_col!r}")
    print(f"  Upper 95% CI:   {upper_col!r}")

    col_map = {
        k: v for k, v in {
            zone_id_col:   "zone_id",
            zone_name_col: "zone_name",
            period_col:    "year_month",
            vol_col:       "avg_daily_volume",
            lower_col:     "lower_95",
            upper_col:     "upper_95",
        }.items() if k is not None
    }
    df_out = combined[list(col_map.keys())].rename(columns=col_map)
    df_out.to_csv(OUTPUT_PREDICTION, index=False)
    print(f"  ✓ {len(df_out)} rows → {OUTPUT_PREDICTION.relative_to(REPO_ROOT)}")
    return df_out


