"""
Streetlight data processing script — Hopkins Street Safety Data Story

Processes Streetlight exports from data/raw/streetlight/ and produces
cleaned, analysis-ready files in data/processed/.

FILE STRUCTURE (actual, confirmed from raw data audit):
  Vehicles:   2012902_Hopkins_Rose_Cedar_AV_2025/
                *_network_performance_seg_metrics.csv  (segment-based, 2025)
                *_network_performance_seg_prediction_interval.csv
  Pedestrian: 2013191_Hopkins_Rose_Cedar_Ped/
                *_za_ped.csv  (zone activity, 2022)
  Bicycle:    2013334_Hopkins_Rose_Cedar_Bike/
                *_za_bike.csv  (zone activity, 2022)

All three modes use StL Volume (confirmed). Cross-mode comparison is valid
in terms of unit type, but structural difference must be noted in all UI:
  - Vehicles: segment-based network performance metrics
  - Ped/bike: zone-based activity metrics

Run from repo root:
  python analysis/streetlight.py

Data integrity: every output value traces directly to a source CSV.
"""

