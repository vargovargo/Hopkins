# STEP 4 — Process network performance: vehicle segments (speed + volume)
# ---------------------------------------------------------------------------

def process_network_performance():
    """
    Read *_network_performance_seg_metrics*.csv — this is the primary vehicle
    volume AND speed data source. (No za_ file exists for vehicles.)

    Saves to data/processed/vehicles_network_performance.csv.
    """
    print("\n" + "=" * 70)
    print("STEP 4 — PROCESS NETWORK PERFORMANCE (vehicle segments)")
    print("=" * 70)

    # Pattern: *_network_performance_seg_metrics.csv (no trailing wildcard segment)
    np_files = sorted(RAW_DIR.rglob("*_network_performance_seg_metrics*.csv"))
    # Exclude monthly files for the primary output
    np_files = [f for f in np_files if "monthly" not in f.name.lower()]

    if not np_files:
        print("  [SKIP] No network performance segment metrics files found.")
        return None, {}, []

    frames = []
    for fpath in np_files:
        df = pd.read_csv(fpath, low_memory=False)
        frames.append(df)
        print(f"  Loaded: {fpath.name}  ({len(df)} rows)")

    combined = pd.concat(frames, ignore_index=True)
    print(f"  All columns: {list(combined.columns)}")

    zone_id_col       = _safe_col(combined, ["Zone ID", "zone_id"])
    zone_name_col     = _safe_col(combined, ["Zone Name", "zone_name"])
    daytype_col       = _safe_col(combined, ["Day Type", "day_type"])
    daypart_col       = _safe_col(combined, ["Day Part", "day_part"])
    period_col        = _safe_col(combined, ["Data Periods", "data_periods"])
    inferred_col      = _safe_col(combined, ["Inferred Volume", "inferred_volume"])

    # Volume: "Average Daily Segment Traffic (StL Volume)"
    seg_traffic_col = None
    for c in combined.columns:
        if "segment traffic" in c.lower() and "average" in c.lower():
            seg_traffic_col = c
            break
        elif "segment traffic" in c.lower():
            seg_traffic_col = c

    # Speed columns — exact names from actual file
    avg_speed_col     = _safe_col(combined, ["Avg Segment Speed (mph)", "Avg Speed (mph)"])
    free_flow_col     = _safe_col(combined, ["Free Flow Speed (mph)"])
    free_flow_fac_col = _safe_col(combined, ["Free Flow Factor"])

    # Speed percentiles — look for "Nth Speed Percentile" pattern
    # Must match each percentile to its own column, not the first match found
    pct_cols = {}
    for pct in [5, 15, 85, 95]:
        for c in combined.columns:
            cl = c.lower()
            if f"{pct}th speed percentile" in cl or f"{pct}th percentile" in cl:
                pct_cols[pct] = c
                break

    print(f"  Segment traffic:   {seg_traffic_col!r}")
    print(f"  Avg speed:         {avg_speed_col!r}")
    print(f"  Free flow speed:   {free_flow_col!r}")
    print(f"  Free flow factor:  {free_flow_fac_col!r}")
    print(f"  Inferred volume:   {inferred_col!r}")
    print(f"  Speed percentiles: {pct_cols}")

    select_map = {k: v for k, v in {
        zone_id_col:       "zone_id",
        zone_name_col:     "zone_name",
        period_col:        "data_periods",
        daytype_col:       "day_type",
        daypart_col:       "day_part",
        seg_traffic_col:   "segment_traffic",
        avg_speed_col:     "avg_speed_mph",
        free_flow_col:     "free_flow_speed_mph",
        free_flow_fac_col: "free_flow_factor",
        inferred_col:      "inferred_volume",
    }.items() if k is not None}
    for pct, col in pct_cols.items():
        select_map[col] = f"speed_p{pct}"

    df_out = combined[list(select_map.keys())].rename(columns=select_map)
    df_out.to_csv(OUTPUT_VEHICLES_NP, index=False)
    print(f"  ✓ {len(df_out)} rows → {OUTPUT_VEHICLES_NP.relative_to(REPO_ROOT)}")

    zones = sorted(df_out["zone_name"].dropna().unique().tolist())
    print(f"  Zones/segments: {zones}")

    if "speed_p85" in df_out.columns:
        print("\n  ★ speed_p85 = 85th percentile — California CVC §22358.5 standard")

    inferred_zones = []
    if "inferred_volume" in df_out.columns:
        inferred_zones = df_out[
            df_out["inferred_volume"].astype(str).str.lower().isin(["yes", "true", "1", "y"])
        ]["zone_name"].unique().tolist()
        if inferred_zones:
            print(f"\n  ⚠️  INFERRED VOLUME ZONES: {inferred_zones}")

    return df_out, pct_cols, inferred_zones


